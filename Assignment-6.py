#Create a tuple t1=(10,20,30,40,30,50) and perform the following 
#i. Find the index of number 40 
#ii. Count for the number 30 
#iii. Delete the tuple.


# Create a tuple
t1 = (10, 20, 30, 40, 30, 50)

# Find the index of number 40
index_40 = t1.index(40)
print("Index of 40:", index_40)

# Count for the number 30
count_30 = t1.count(30)
print("Count of 30:", count_30)

# Delete the tuple
del t1


